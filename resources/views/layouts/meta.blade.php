    function getCurrentLocaleFromPath(path) {
        // URLni `/` bilan bo'lib, birinchi elementni olish.
        return path.split('/')[1];
    }

    function switchLocale(locale) {
    let currentUrl = new URL(window.location.href);
    let currentLocale = getCurrentLocaleFromPath(currentUrl.pathname);

    // URL manzilining ma'lum qismlari uchun tekshiruv
    if (currentUrl.pathname.includes('/news/') || 
        currentUrl.pathname.includes('/project/') || 
        currentUrl.pathname.includes('/service/')) {
        // Foydalanuvchini bosh sahifaga yo'naltirish
        window.location.href = `${currentUrl.origin}/${locale}`;
    } else {
        // Hozirgi sahifada qoldirish
        let newPathname = currentUrl.pathname.replace('/' + currentLocale, '/' + locale);
        window.location.href = currentUrl.origin + newPathname + currentUrl.search;
    }
}

</script>


